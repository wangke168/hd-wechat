                <p class="">2 月 17 日的耐克波特兰全球总部，戴着黑框圆眼镜、穿着黑衬衫的 John Hoke
                    带着他治下的三个设计主管，一起坐在有着大大的三个黑三角的一面白墙前，每个三角里都摆着一只有着透明鞋底的灰色球鞋。<br></p>

                <p>同样的鞋在设计师们面前的原木桌上也摆着三四只，周围散落着各种形状奇怪的东西：更多透明底的球鞋，更多透明的鞋垫，还有耐克公司历史上最著名的产品之一：联合创始人比尔·鲍尔曼 (Bill Bowerman)
                    教练的华夫底球鞋。</p>

                <p>John Hoke
                    是耐克全球设计副总裁，他戴上不知为何小了几码的白手套，拿起鲍尔曼的华夫鞋，开始解释耐克的设计纲领。第一句话媒体们绝不陌生，它曾经在耐克各类介绍新产品的场合出现过，还以巨大装饰字样挂在耐克设计部门大楼的一整面墙上：“永远倾听运动员的声音”——每当这句话在一个挤满媒体的房间里响起的时候，就是耐克发布新产品的时刻。这一次的主角，则是墙上的球鞋：Vapormax。</p>

